/*
A literal is a literal representation of a value in javascript. It is a fixed value - not a variable - they are literally provided in the code.

A variable is not a literal, the information assigned to the variable is the literal.
    ex:
      (keyword)               (value / literal)
        let       placeholder = 'text';
                                (variable)
*/

// String Literal
let city = 'Indianapolis';

// Numeric Literal
let num = 123;

// Boolean Literal
let isOn = true;

// Object Literal
let movie = {
    title: 'Elf',
    rating: 'PG',
    release:'November 7th 2003'
};

// Array Literal
let daysOfWeek = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];